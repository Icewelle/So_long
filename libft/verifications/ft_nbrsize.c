#include "libft.h"

size_t	ft_nbrsize(unsigned long nbr)
{	
	size_t	i;

	i = 0;
	if (nbr == 0)
		return (1);
	while (nbr != 0)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}